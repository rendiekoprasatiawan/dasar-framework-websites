Apa itu laravel
- laravel adalah sebuah framework php
- untuk pengembangan aplikasi web dan API
- Tidak ada biaya
- Framework yang modern dan mudah untuk digunakan
- Framework PHP yang paling banyak digunakan

Sejarah Laravel dan versi saat ini
- Pertama kali release : Juni 2011
- Pencipta : Taylor Otwell
- Ditulis dalam : PHP Language
- Versi Laravel saat ini ditulis : 8.0

Kenapa kita harus menggunakan laravel?
- Dukungan yang kuat kalau untuk command line (cukup belajar artisan deh)
- Komunitas yang gede
- Pengupdatean secara berkala, jika ada bug or something else. Even gak ada biasanya tetap update untuk fitur baru
- Cepat untuk sekelas framework yang baik

