Installation laravel memakai composer
- Bagian ini kita mulai untuk menginstall laravel dan ini sangat mudah
- pertama ketikkan : composer create-project laravel/laravel laravel-news
- kedua ketikkan : cd laravel-news
- untuk menjalankan yang sudah kita retrieve : php artisan serve

#notes : laravel-news -> itu adalah nama folder (bisa di-custom ya gengs)
